  async ensureFolder(folderPath) {
    if (!folderPath) {
      return;
    }
    const parts = (0, import_obsidian3.normalizePath)(folderPath).split("/").filter(Boolean);
    let current = "";
    for (const part of parts) {
      current = current ? `${current}/${part}` : part;
      if (!await this.app.vault.adapter.exists(current)) {
        await this.app.vault.createFolder(current);
      }
    }
  }
  async fileExists(path) {
    return this.app.vault.adapter.exists((0, import_obsidian3.normalizePath)(path));
  }
  async removeFile(path) {
    const normalized = (0, import_obsidian3.normalizePath)(path);
    if (await this.app.vault.adapter.exists(normalized)) {
      await this.app.vault.adapter.remove(normalized);
    }
  }
