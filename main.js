    if (validationError) {
      new import_obsidian4.Notice(validationError, 8e3);
      return null;
    }
    this.syncPromise = this.runSync(trigger);
    const result = await this.syncPromise.finally(() => {
      this.syncPromise = null;
      this.updateStatusBar();
      this.settingsTab?.display();
    });
