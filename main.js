function parentPath(path) {
  const segments = (0, import_obsidian3.normalizePath)(path).split("/");
  segments.pop();
  return segments.join("/");
}
function sortByMostRecent(left, right) {
  return compareTimestamps(left.updatedAt ?? left.createdAt, right.updatedAt ?? right.createdAt);
}
function compareTimestamps(left, right) {
  if (!left && !right) {
    return 0;
  }
  if (!left) {
    return -1;
  }
  if (!right) {
    return 1;
  }
  return left.localeCompare(right);
}
function inferExtension(name, url, contentType) {
  const fromName = extractExtension(name);
  if (fromName) {
    return fromName;
  }
  const fromUrl = extractExtension(url);
  if (fromUrl) {
    return fromUrl;
  }
  const lowered = (contentType ?? "").toLowerCase();
  const mapping = {
    "image/jpeg": "jpg",
    "image/png": "png",
    "image/gif": "gif",
    "image/webp": "webp",
    "audio/mpeg": "mp3",
    "audio/mp4": "m4a",
    "audio/wav": "wav",
    "video/mp4": "mp4",
    "application/pdf": "pdf"
  };
  return mapping[lowered] ?? "";
}
function extractExtension(value) {
  try {
    const pathname = value.includes("://") ? new URL(value).pathname : value;
    const match = pathname.match(/\.([a-zA-Z0-9]{1,10})$/);
    return match?.[1]?.toLowerCase() ?? "";
  } catch {
    return "";
  }
}
function stripExtension(value) {
  return value.replace(/\.[a-zA-Z0-9]{1,10}$/, "");
}
function ensureExtension(value, extension) {
  return value.toLowerCase().endsWith(`.${extension.toLowerCase()}`) ? value : `${value}.${extension}`;
}
function isEmbeddable(extension, contentType) {
  const loweredExtension = extension.toLowerCase();
  const loweredType = (contentType ?? "").toLowerCase();
  return ["png", "jpg", "jpeg", "gif", "webp", "svg", "mp3", "m4a", "wav", "mp4", "pdf"].includes(
    loweredExtension
  ) || loweredType.startsWith("image/") || loweredType.startsWith("audio/") || loweredType.startsWith("video/") || loweredType === "application/pdf";
}

